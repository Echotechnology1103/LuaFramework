		for k,v in pairs(attr) do
			if v > 0 then
				table.insert(read_data, {key = k, value = v, sort = sort_list[k] or 0})
			end
		end

		function sort_attr(a, b)
			return a.sort < b.sort
		end

		table.sort(read_data, sort_attr)

		view_name = view_name or Language.JingLing.AttrTipTitle
		if #read_data > 7 then
			self.general_attr_all_view:SetData(read_data, view_name)
		else
			self.general_attr_small_view:SetData(read_data, view_name)
		end
	end
end

