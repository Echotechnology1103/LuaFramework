		if self.tips_buff_pandect_view:IsOpen() then
			self.tips_buff_pandect_view:PauseTimer()
		else
			local data = FightData.Instance:GetMainRoleShowEffect() or {}
			for k,v in pairs(data) do
				if v ~= nil then
					if v.info and v.info.client_effect_type then
						local client_type = v.info.client_effect_type
						local cfg = FightData.Instance:GetBuffDescCfgByType(client_type)
						if cfg ~= nil and cfg.dead_pause == 1 then
							FightData.Instance:SetBuffPause(client_type, Status.NowTime)
						end
					end
				end
			end
		end
	end
