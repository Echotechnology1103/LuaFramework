		self.auto_view:SetRedText(the_red_text)
		self.auto_view:SetIsUseMaskClick(use_mask_close or 0)
		if self.auto_view:GetAutoClose() then
			ok_callback(true)
		else
			self.auto_view:Open()
		end
	end
