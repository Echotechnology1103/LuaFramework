function TipsCtrl:ShowCommonAutoView(auto_str, des, ok_callback, cancel_callback, show_red_tip, ok_des, canel_des, the_red_text, is_special, is_on, use_mask_close)
	auto_str = auto_str or ""
	local auto_view_str = self.auto_view.auto_view_str
	if (self.auto_view.is_auto and auto_str ~= "" and auto_str == auto_view_str) or TipsCommonAutoView.AUTO_VIEW_STR_T[auto_str] then
		if ok_callback then
			ok_callback(true)
		end
	else
		if auto_str ~= "" then
			self.auto_view:SetAutoStr(auto_str)		-- 替换自动购买唯一标示
			self.auto_view:SetShowAutoBuy(true)
		else
			self.auto_view:SetShowAutoBuy(false)
		end
		self.auto_view:SetShowRedTip(show_red_tip and true or false)
		self.auto_view:SetDes(des)
		self.auto_view:SetIsSpecial(is_special)
		self.auto_view:SetOkCallBack(ok_callback)
		self.auto_view:SetCanelCallBack(cancel_callback)
