local ClientDefaultMessages = {}
local value_number = 1
local value_string = 2
local value_table = 3

function buildDefaultClientMessage()
	local tmpbaseTypes = {
		"bool",
		"int8",
		"uint8",
		"int16",
		"uint16",
		"int32",
		"uint32",
		"int64",
		"uint64",
		"float",
		"double"
	}
	local baseTypes = {}

	for k, v in ipairs(tmpbaseTypes) do
		baseTypes[v] = true
	end

	for i = 1, #CMProtocals, 1 do
		CMProtocals[i].Params = CMProtocals[i].Params or {}
		local cmsg = CMProtocals[i]
		local defaultParam = {}

		for j = 1, #cmsg.Params, 1 do
			local paramType = cmsg.Params[j][2]

			if baseTypes[paramType] then
				defaultParam[cmsg.Params[j][1]] = value_number
			elseif paramType == "string" then
				defaultParam[cmsg.Params[j][1]] = value_string
			else
				defaultParam[cmsg.Params[j][1]] = value_table
			end
		end

		ClientDefaultMessages[cmsg.ClassId] = defaultParam
	end

	return 
end

function DefaultClientMessage(protId)
	local cmsg = {
		Cmd = protId
	}
	local timeinterval = 0
	local msg = ""

	if ClientDefaultMessages[protId] then
		for k, v in pairs(ClientDefaultMessages[protId]) do
			if v == value_number then
				cmsg[k] = 0
			elseif v == value_string then
				cmsg[k] = ""
			else
				cmsg[k] = {}
			end
		end
	else
		print("Protocols DefaultClientMessage ERROR: No Such Client Message(" .. protId .. ")!!!")
	end

	return cmsg
end

