CChiBang = class("CChiBang", nil)
function CChiBang:ctor(roleShapeId, cbTypeId, roleNode)
  self.m_RoleShapeId = roleShapeId
  self.m_CbTypeId = cbTypeId
  self.m_RoleZOrder = roleNode:getZOrder()
  local parentNode = roleNode:getParent()
  local cbTypeShapeId = data_getWingShapeId(cbTypeId)
  self.m_ChiBang_1 = CreateSeqAnimation(string.format("xiyou/chibang/cb_%d_1.plist", cbTypeShapeId), -1)
  if parentNode.addNode ~= nil then
    parentNode:addNode(self.m_ChiBang_1, self.m_RoleZOrder)
  else
    parentNode:addChild(self.m_ChiBang_1, self.m_RoleZOrder)
  end
  self.m_ChiBang_2 = CreateSeqAnimation(string.format("xiyou/chibang/cb_%d_2.plist", cbTypeShapeId), -1)
  if parentNode.addNode ~= nil then
    parentNode:addNode(self.m_ChiBang_2, self.m_RoleZOrder)
  else
    parentNode:addChild(self.m_ChiBang_2, self.m_RoleZOrder)
  end
  self.m_Pos = ccp(0, 0)
end
function CChiBang:getShapeId()
  return self.m_RoleShapeId
end
function CChiBang:SetActAndDir(actName, dir)
  local act = string.format("%s_%d", actName, dir)
  self.m_ChiBang_1:playAniWithName(act)
  self.m_ChiBang_2:playAniWithName(act)
  local data = ChiBangDeltaZOrder[self.m_RoleShapeId] or {}
  local info = data[actName] or {}
  local d = info[dir] or {}
  local detaz_1 = d[1] or 0
  local detaz_2 = d[2] or 0
  self.m_ChiBang_1:getParent():reorderChild(self.m_ChiBang_1, self.m_RoleZOrder + detaz_1)
  self.m_ChiBang_2:getParent():reorderChild(self.m_ChiBang_2, self.m_RoleZOrder + detaz_2)
end
function CChiBang:Clear()
  self.m_ChiBang_1:removeFromParent()
  self.m_ChiBang_2:removeFromParent()
end
function CChiBang:setVisible(v)
  self.m_ChiBang_1:setVisible(v)
  self.m_ChiBang_2:setVisible(v)
end
function CChiBang:setPosition(x, y)
  if x ~= nil and y ~= nil then
    self.m_ChiBang_1:setPosition(x, y)
    self.m_ChiBang_2:setPosition(x, y)
    self.m_Pos = ccp(x, y)
  elseif x ~= nil and y == nil then
    self.m_ChiBang_1:setPosition(x.x, x.y)
    self.m_ChiBang_2:setPosition(x.x, x.y)
    self.m_Pos = x
  end
end
function CChiBang:getPosition()
  return self.m_Pos.x, self.m_Pos.y
end
function CChiBang:setColor(c)
end
function CChiBang:setOpacity(opacity)
  self.m_ChiBang_1:setOpacity(opacity)
  self.m_ChiBang_2:setOpacity(opacity)
end
function CChiBang:setScaleX(s)
  self.m_ChiBang_1:setScaleX(s)
  self.m_ChiBang_2:setScaleX(s)
end
