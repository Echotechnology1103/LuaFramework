function GetZuoqiPngShapeId(roleShapeId, zqTypeId)
  local race = math.floor(roleShapeId % 10000 / 1000)
  local pngZuoqiShape = zqTypeId
  local zqKey = 11
  if zqTypeId % 10 == 1 then
    zqKey = 11
  elseif zqTypeId % 10 == 2 then
    zqKey = 12
  elseif zqTypeId % 10 == 3 then
    zqKey = 21
  elseif zqTypeId % 10 == 4 then
    zqKey = 22
  elseif zqTypeId % 10 == 5 then
    zqKey = 31
  else
    zqKey = 41
  end
  if race == 1 then
    pngZuoqiShape = 9100 + zqKey
  elseif race == 2 then
    pngZuoqiShape = 9200 + zqKey
  elseif race == 3 then
    pngZuoqiShape = 9300 + zqKey
  elseif race == 4 then
    pngZuoqiShape = 9400 + zqKey
  end
  return pngZuoqiShape
end
CZuoQiShapeAni = class("CZuoQiShapeAni", nil)
function CZuoQiShapeAni:ctor(roleShapeId, zqTypeId, roleNode)
  self.m_RoleShapeId = roleShapeId
  local pngZuoqiShape = GetZuoqiPngShapeId(roleShapeId, zqTypeId)
  self.m_ZuoqiShapeId = pngZuoqiShape
  self.m_RoleZOrder = roleNode:getZOrder()
  local parentNode = roleNode:getParent()
  self.m_ZuoqiDown = CreateSeqAnimation(string.format("xiyou/shape/shape%s_down.plist", pngZuoqiShape), -1)
  if parentNode.addNode ~= nil then
    parentNode:addNode(self.m_ZuoqiDown, self.m_RoleZOrder)
  else
    parentNode:addChild(self.m_ZuoqiDown, self.m_RoleZOrder)
  end
  self.m_ZuoqiUp = CreateSeqAnimation(string.format("xiyou/shape/shape%s_up.plist", pngZuoqiShape), -1)
  if parentNode.addNode ~= nil then
    parentNode:addNode(self.m_ZuoqiUp, self.m_RoleZOrder)
  else
    parentNode:addChild(self.m_ZuoqiUp, self.m_RoleZOrder)
  end
  self.m_Pos = ccp(0, 0)
end
function CZuoQiShapeAni:getShapeId()
  return self.m_RoleShapeId
end
