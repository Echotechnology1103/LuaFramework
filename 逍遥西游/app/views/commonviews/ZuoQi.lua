function CZuoQiShapeAni:setPosition(x, y)
  local upPosDelX = 0
  local upPosDelY = 0
  if self.m_ZuoqiShapeId == 9121 or self.m_ZuoqiShapeId == 9122 then
    upPosDelY = -20
  elseif (self.m_ZuoqiShapeId == 9321 or self.m_ZuoqiShapeId == 9322) and self.m_ActName == "walk" and self.m_DirName == 2 then
    upPosDelX = 5
    upPosDelY = 5
    if self.m_ScaleX == -1 then
      upPosDelX = 0 - upPosDelX
    end
  end
  if x ~= nil and y ~= nil then
    self.m_ZuoqiDown:setPosition(x, y)
    self.m_ZuoqiUp:setPosition(x + upPosDelX, y + upPosDelY)
    self.m_Pos = ccp(x, y)
  elseif x ~= nil and y == nil then
    self.m_ZuoqiDown:setPosition(x.x, x.y)
    self.m_ZuoqiUp:setPosition(x.x + upPosDelX, x.y + upPosDelY)
    self.m_Pos = x
  end
end
