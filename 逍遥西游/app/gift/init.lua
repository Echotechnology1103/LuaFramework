local online = import(".GiftOfOnline")
local levelup = import(".GiftOfLevelup")
local checkin = import(".GiftOfCheckin")
local identify = import(".GiftOfIdentify")
local festival = import(".GiftOfFestival")
local special = import(".GiftOfSpecial")
local newTermCheckIn = import(".GiftOfNewTermCheckIn")
local guoQingCheckIn = import(".GiftOfGuoQingCheckIn")
gift = {}
gift.online = online.new()
gift.levelup = levelup.new()
gift.checkin = checkin.new()
gift.identify = identify.new()
gift.festival = festival.new()
gift.special = special.new()
gift.newTermCheckIn = newTermCheckIn.new()
gift.guoQingCheckIn = guoQingCheckIn.new()
function gift.acceptGiftResult(giftId, resultFlag)
  SendMessage(MsgID_Gift_AcceptRewardResult, giftId, resultFlag == 1)
end
gamereset.registerResetFunc(function()
  if gift.festival then
    gift.festival:Clean()
  end
  gift.online = online.new()
  gift.levelup = levelup.new()
  gift.checkin = checkin.new()
  gift.identify = identify.new()
  gift.festival = festival.new()
  gift.special = special.new()
  gift.newTermCheckIn = newTermCheckIn.new()
  gift.guoQingCheckIn = guoQingCheckIn.new()
end)
