local CTestWarRoleCreator = class("CTestWarRoleCreator", CRoleFactory)
function CTestWarRoleCreator:ctor()
  CTestWarRoleCreator.super.ctor(self)
end
g_TestWarRoleCreator = CTestWarRoleCreator.new()
function testGetOnePet(typeId, lv, teamType)
  local pet = g_TestWarRoleCreator:newObject(getInsId(LOGICTYPE_PET), typeId)
  pet:setProperty(PROPERTY_ROLELEVEL, lv)
  pet:setProperty(PROPERTY_GenGu, lv)
  pet:setProperty(PROPERTY_Lingxing, lv + lv * 4)
  pet:setProperty(PROPERTY_LiLiang, lv)
  pet:setProperty(PROPERTY_MinJie, lv)
  pet:setProperty(PROPERTY_RANDOM_GROWUP, 10)
  pet:addZhuangBei(10001, false)
  pet:delZhuangBei(10001, false)
  pet:addZhuangBei(10003, false)
  pet:addZhuangBei(10002, false)
  pet:setProperty(PROPERTY_TEAM, teamType)
  pet:CalculateProperty()
  return pet
end
