local ShowLogin, ShowReg
local function ShowTestStart()
  local view = CcsUIView.new("views/testGameStart.json")
  view:ShowAsScene()
  view:addBtnListener("btn_login", function()
    print("===>> login")
    ShowLogin()
  end)
  view:addBtnListener("btn_reg", function()
    print("===>> reg")
    ShowReg()
  end)
end
function ShowLogin()
  local view_log = CcsUIView.new("views/testview_login.json")
  view_log:ShowAsScene()
  view_log:addBtnListener("btn_login", function()
    print("===>> login")
  end)
  view_log:addBtnListener("btn_reg", function()
    print("===>> btn_reg")
    ShowReg()
  end)
end
function ShowReg()
  local view_log = CcsUIView.new("views/testview_reg.json")
  view_log:ShowAsScene()
  view_log:addBtnListener("btn_reg", function()
    print("===>> btn_reg")
  end)
  view_log:addBtnListener("btn_cancel", function()
    print("===>> btn_cancel")
    ShowLogin()
  end)
end
