local NilBehavior = { type = 1, value = 0 };
i3k_entity_behavior = i3k_class("i3k_entity_behavior");
function i3k_entity_behavior:ctor(entity)
	self._entity	= entity;
	self._value		= { };
end

function i3k_entity_behavior:Set(bh, bid, vtype, val, caller)
	local add	 = true;
	local update = false;
	local tbl = gEBMatchTbl[bh];
	local tbClear = {};

	if tbl then
		for k = eEBIdle, eEBCount do
			if self:Test(k) then
				local sm = tbl[k];
				if sm then
					if sm == eStateCoexist then		-- 并存
					elseif sm == eStateMatch then	-- 冲抵
						table.insert(tbClear, { bnum = k})
					elseif sm == eStateImmune then -- 免疫
						add = false;
					elseif sm == eStateUpdate then -- 更新
						update = true;
					end
				end
			end
		end
	end

	if add then

		local _bid = -1;
		if bid ~= nil then
			_bid = bid;
		end

		for k,v in ipairs(tbClear) do
			self:Clear(v.bnum, -1, true);
		end

		-- TODO 目前先不做数值的更新处理
		if not self._value[bh] then
			self._value[bh] = { bid = { }, type = vtype or 1, value = val or 0 };
		end

		self._value[bh].bid[_bid]	= _bid;
		if update then
			self._value[bh].type	= vtype or 1;
			self._value[bh].value	= val or 0;
		end

		if update then
			self._entity:OnBehaviorUpdate(caller, bh, self._value[bh]);
		else
			self._entity:OnBehavior(caller, bh, self._value[bh]);
		end
	end

	return add;
end

function i3k_entity_behavior:Update(bh, value)
	if self._value[bh] then
		self._value[bh].value = value;
	end
end

function i3k_entity_behavior:Get(bh)
	return self._value[bh] or NilBehavior;
end

function i3k_entity_behavior:GetValue(bh)
	local item = { type = 1, value = 0 };

	local _v = self._value[bh];
	if _v then
		item.type	= _v.type;
		item.value	= _v.value;
	end

	return item;
end

function i3k_entity_behavior:Consume(bh, val)
	local res = val;

	local _val = self._value[bh];
	if _val then
		if _val.value >= res then
			_val.value = _val.value - res;

			res = 0;
		else
			res = res - _val.value;

			_val.value = 0;
		end
	end

	return res;
end

function i3k_entity_behavior:Empty()
	local len = i3k_table_length(self._value);

	return len == 0;
end

function i3k_entity_behavior:Test(bh)
	local state = self._value[bh];
	if state then
		return true;
	else
		return false;
	end
end

function i3k_entity_behavior:Clear(bh, bid, all)
	local _bid = -1;
	if bid ~= nil then
		_bid = bid;
	end

	local _all = false;
	if all ~= nil then
		_all = all;
	end

	if self._value[bh] then
		if _all then
			if self._entity then
				for k, v in pairs(self._value[bh].bid) do
					self._entity:ClsBuff(v);
				end
				self._entity:OnLeaveBehavior(bh);
			end

			self._value[bh] = nil;
		else
			self._value[bh].bid[_bid] = nil;

			if i3k_table_length(self._value[bh].bid) == 0 then
				if self._entity then
					self._entity:OnLeaveBehavior(bh);
				end

				self._value[bh] = nil;
				if self._entity then
					self._entity:OnClearAfterBehavior(bh);
				end
			end
		end
	end
end

function i3k_entity_behavior:ClearAll(exclude)
	local removed = { };

	for bh, v in pairs(self._value) do
		local remove = true;

		if exclude then
			for k = 1, #exclude do
				local v1 = exclude[k];
				if v1 == bh then
					remove = false;
				end
			end
		end

		if remove then
			table.insert(removed, bh);
		end
	end

	for k = 1, #removed do
		local bh = removed[k];

		if self._entity then
			self._entity:OnLeaveBehavior(bh);
		end
		self._value[bh] = nil;
	end
end
