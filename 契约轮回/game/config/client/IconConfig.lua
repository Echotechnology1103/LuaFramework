local function HandleConfig(config)
    for key, config in pairs(config) do
        local id = config.id .. "@" .. config.sub_id
        if not config.key_str then
            config.key_str = key
        end
        if not config.icon and config.key_str then
            config.icon = IconConfig[config.key_str]
        end
        if not config.level and config.key_str then
            config.level = OpenConfig[id] and OpenConfig[id].level
        end
        if not config.task and config.key_str then
            config.task = OpenConfig[id] and OpenConfig[id].task
        end
    end
end

