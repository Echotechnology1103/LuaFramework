local function HandlerConfig()
    for scene_id, list in pairs(SceneEffectConfig) do
        for index, v in pairs(list) do
            v.id = index
            v.coord = { x = v.x, y = v.y }
            v.uid = "effect_" .. scene_id .. "_" .. v.id
            v.type = enum.ACTOR_TYPE.ACTOR_TYPE_EFFECT
            v.gen_type = SceneConstant.DataType.Client
        end
    end
end
