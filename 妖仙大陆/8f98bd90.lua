function GlobalHooks.GetUITAG(key)
    return GlobalHooks.UITAG[key] or 0
end

function GlobalHooks.GetUIKey(tag)
    for key,v in pairs(GlobalHooks.UITAG or {}) do
        if v == tag then
            return key
        end
    end
    return ''
end
