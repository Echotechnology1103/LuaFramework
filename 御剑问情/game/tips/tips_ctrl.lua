		return
	end

	if not CompetitionActivityData.Instance:GetBiPinOpen(tab_index) then return end

	local zsd_seq = DailyChargeData.Instance:GetCanGetZhishengdanSeq()
	if zsd_seq >= 0 then
		self.tips_zhishengdan_view:SetData(tab_index, cur_grade)
