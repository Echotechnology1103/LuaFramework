function TipsCtrl:OpenItem(data, from_view, param_t, close_call_back, show_the_random, gift_id, is_check_item, is_tian_sheng)

	if data == nil then
		return
	end

	local item_cfg, big_type = ItemData.Instance:GetItemConfig(data.item_id)
	if item_cfg == nil then
		return
	end

	--小鬼守护相关的物品单独处理
	local imp_guard_cfg_info = PlayerData.Instance:GetImpGuardCfgInfoByItemId(data.item_id)
	if imp_guard_cfg_info then
		self.tips_timelimit_equip_view:SetData(data)
		self.tips_timelimit_equip_view:SetFrom(from_view)
		self.tips_timelimit_equip_view:SetCloseCallBack(close_call_back)
		self.tips_timelimit_equip_view:Open()
		return
	end

	if item_cfg.use_type == GameEnum.ITEM_OPEN_TITLE then
		self.display_prop_modle_view:SetData(data, from_view, param_t, close_call_back)
		return
	end

	if item_cfg.use_type == GameEnum.USE_TYPE_LITTLE_PET then
		self.tips_little_pet_equip_view:SetData(data, from_view, param_t, close_callback)
		return
	end

	if item_cfg.is_display_role and item_cfg.is_display_role ~= "" and item_cfg.is_display_role > 0 and item_cfg.is_display_role ~= DISPLAY_TYPE.TITLE then
		if item_cfg.sub_type == GameEnum.EQUIP_TYPE_JINGLING then
			self.spirit_equip_view:SetData(data,from_view, param_t,close_call_back)
		else
			self.display_prop_modle_view:SetData(data, from_view, param_t, close_call_back)
		end
		return
	end

	local gamoe_vo = GameVoManager.Instance:GetMainRoleVo()
	if item_cfg.sub_type == GameEnum.EQUIP_TYPE_JINGLING then
		self.other_equip_tips:Close()
		self.prop_tips:Close()
		self.spirit_equip_view:SetData(data, from_view, param_t,close_call_back)
	elseif CardData.Instance:IsCardPiece(data.item_id) then
		CardCtrl.Instance:OpenCardPieceTip(data, from_view, param_t, close_call_back, gift_id, is_check_item)
	elseif big_type == GameEnum.ITEM_BIGTYPE_EQUIPMENT then					--装备
		local equip_index = EquipData.Instance:GetEquipIndexByType(item_cfg.sub_type)
		if equip_index >= 0 then
			if from_view == TipsFormDef.FROME_MARKET_GOUMAI and EquipData.Instance:GetGridData(equip_index)
				and EquipData.Instance:GetGridData(equip_index).item_id and (param_t == nil or param_t.role_id == nil or
					param_t.role_id ~= gamoe_vo.role_id) then
				self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
				return
			end
			if EquipData.Instance:GetGridData(equip_index) and EquipData.Instance:GetGridData(equip_index).item_id and
				from_view ~= TipsFormDef.FROM_BAG_EQUIP and from_view ~= TipsFormDef.FROME_BROWSE_ROLE and (param_t == nil or param_t.role_id == nil or
				param_t.role_id ~= gamoe_vo.role_id) and (item_cfg.limit_prof == gamoe_vo.prof or item_cfg.limit_prof == 5) then
				self.other_role_equip_view:SetData(data, from_view, param_t, close_call_back, gift_id, is_check_item)
			else
				self.equip_tips:SetData(data, from_view, param_t, close_call_back, gift_id)
			end
		elseif EquipData.IsMarryEqType(item_cfg.sub_type) then
			local marry_eq_index = MarryEquipData.GetMarryEquipIndex(item_cfg.sub_type)
			local my_data = MarryEquipData.Instance:GetMarryEquipInfo()[marry_eq_index]
			if my_data and next(my_data) and my_data.item_id > 0 and from_view ~= TipsFormDef.FROM_BAG_EQUIP
			and item_cfg.limit_sex == gamoe_vo.sex then
				self.other_equip_compare_view:SetData(data, from_view, param_t, close_call_back, is_tian_sheng)
			else
				self.other_equip_tips:SetData(data, from_view, param_t, close_call_back, show_the_random, is_tian_sheng)
			end
		else
			local zs_equip_index = ZhuanShengData.Instance:GetZhuanShengEquipIndex(item_cfg.sub_type)
			local my_data = ZhuanShengData.Instance:GetDressEquipList()[zs_equip_index]
			if my_data and next(my_data) and my_data.item_id > 0 and from_view ~= TipsFormDef.FROM_ZHUANSHENG_VIEW then
				self.other_equip_compare_view:SetData(data, from_view, param_t, close_call_back, is_tian_sheng)
			else
				self.other_equip_tips:SetData(data, from_view, param_t, close_call_back, show_the_random, is_tian_sheng)
			end
		end
		self.prop_tips:Close()
	elseif MojieData.IsMojie(data.item_id) and from_view == TipsFormDef.FROM_BAG_EQUIP then
		self.mojie_tips:SetData(data, from_view, param_t,close_call_back)
	else
		self.equip_tips:Close()
		self.prop_tips:SetData(data, from_view, param_t,close_call_back)
	end
end

