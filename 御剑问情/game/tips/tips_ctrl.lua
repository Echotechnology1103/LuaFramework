--ease为速率曲线(http://robertpenner.com/easing/easing_demo.html), duration为时间, need_repeat是否需要重复回调（每个粒子完成移动都会回调一次）effect_count 可为空，默认为8
function TipsCtrl:ShowFlyEffectManager(from_view, bundle, asset, start_obj, end_obj, ease, duration, callback, need_repeat, effect_count)
	asset = asset or "UI_guangdian1"
	bundle = bundle or "effects2/prefab/ui_x/" .. string.lower(asset) .. "_prefab"

	ease = ease or DG.Tweening.Ease.Linear
	duration = duration or 1
	local view = FlyEffectView.New()
	view:SetAsset(bundle, asset)
	view:SetStartObj(start_obj)
	view:SetEndObj(end_obj)
	view:SetEase(ease)
	view:SetDuration(duration)
	view:SetCompleteCallBack(callback)
	view:SetNeedRepeat(need_repeat)
	view:SetEffectCount(effect_count)
	view:Open()

	if nil == self.fly_effect_list[from_view] then
		self.fly_effect_list[from_view] = {}
	end
	table.insert(self.fly_effect_list[from_view], view)
end

function TipsCtrl:DestroyFlyEffectByViewName(from_view)
	if self.fly_effect_list[from_view] then
		for _, v in ipairs(self.fly_effect_list[from_view]) do
			v:DeleteMe()
		end
		self.fly_effect_list[from_view] = nil
	end
end

